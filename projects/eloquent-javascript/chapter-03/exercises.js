////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  return num1 < num2 ? num1 : num2;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  num = Math.abs(num);
  if (!num)
    return true;
  else if (num === 1)
    return false;
  return isEven(num - 2);
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  const splitStr = string.split('');
  return splitStr.reduce((counter, current) => {
    if (current === char) 
      counter++;
    return counter;
  }, 0);
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  const splitStr = string.split('');
  return splitStr.reduce((counter, char) => {
    if (char === 'B')
      counter++;
    return counter;
  }, 0);
}

