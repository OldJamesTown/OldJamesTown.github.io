////////////////////////////////////////////////////////////////////////////////

function drawChessboard(num) {
let board = '';

for (let y = 0; y < num; y++) {
  for (let x = 0; x < num; x++) {
    if ((x + y) % 2 == 0) 
      board += ' ';
    else 
      board += '#';
  } board += '\n';
} console.log(board);
}

